Map.prototype = {
	nextToWall: function( x, y, direction )
	{
		if ( direction === 'down' )
		{
			y += 1;
		}
		else if ( direction === 'up' )
		{
			y -= 1;
		}
		else if ( direction === 'right' )
		{
			x += 1;
		}
		else if ( direction === 'left' )
		{
			x -= 1;
		}

		return this.tiles[ x + y * this.width ] === 1;
	}
};